package com.roopasoft.jobs;

public class JobsDetailsInfo
{

	private String title;
	private String category;
	private String job_type;
	private String budget;
	private String snippet;
	private String skills_indetail;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getJob_type() {
		return job_type;
	}
	public void setJob_type(String job_type) {
		this.job_type = job_type;
	}
	public String getBudget() {
		return budget;
	}
	public void setBudget(String budget) {
		this.budget = budget;
	}
	public String getSnippet() {
		return snippet;
	}
	public void setSnippet(String snippet) {
		this.snippet = snippet;
	}
	public String getSkills_indetail() {
		return skills_indetail;
	}
	public void setSkills_indetail(String skills_indetail) {
		this.skills_indetail = skills_indetail;
	}
	
	
}
